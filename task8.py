"""
8. Відомо, що X кг цукерок коштують A гривень. Визначити, скільки коштує 1 кг і Y кг цих же
цукерок.
Іванова Дар'я 141 група
"""

def candy():
    x = float(input('введите x кг='))
    a = float(input('введите a грн='))
    # скільки коштує 1 кг
    d = float((1*a)/x)
    # скільки коштує Y кг
    y = float(input('введите y кг='))
    k = float((y*a)/x)
    return "d(цена за 1кг)=" + str(d) + "\nk(цена за y кг)=" + str(k)


print(candy())
